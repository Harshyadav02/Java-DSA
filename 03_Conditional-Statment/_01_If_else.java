class _01_If_else{
	public static void main(String args[]){

		// If - Else are used for condition 

		/* 

			If the condition is true then the if block will execute , otherwise else block will execute

			Note :- we can write multiple if , but there will be only else
		*/

		int x = 10;
		
		// In brackets we write condition
		if(x > 5){
			System.out.println("X is greater");
		}
		
		if (x == 5) {
			System.out.println("X is eqaul to 5");
		}
		else{
			System.out.println("X is smaller");
		}

		// System.out.println(y);
		System.out.println();
	}
}