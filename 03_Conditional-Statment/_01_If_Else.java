class _01_If_Else{
	public static void main(String args[]){

		// If - Else are used for condition 

		/* 

			If the condition is true then the if block will execute , otherwise else block will execute

			Note :- we can write multiple if , but there will be only else
		*/

		int number = 10;
		
		// In brackets we write condition
		if(number > 5){
			System.out.println("Number is greater");
		}
		
		if (number == 5) {
			System.out.println("Number is eqaul to 5");
		}
		else{
			System.out.println("Number is smaller");
		}

		// System.out.println(y);
		System.out.println();
	}
}
