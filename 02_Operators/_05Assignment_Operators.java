class _05Assignment_Operators{
	public static void main(String[] args) {
		
		/* 
			Assignment Operators


			=  
			+=
			-=
			*=
			/=




		*/

		// Use of  = Operator

			int a  = 10;

		// use of += Operator

			a += 1; // output 11

		// use of -= Operator

			int  b = 10;

			b -= 1; // output 9

		// use of *= Operator

			a *= 2; //output 22

		// use of /= Operator

			b  /= 2 ; //output 4

	}
}