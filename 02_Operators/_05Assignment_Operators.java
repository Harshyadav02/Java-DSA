class _05Assignment_Operators{
	public static void main(String[] args) {
		
		/* 
			Assignment Operators


			=  
			+=
			-=
			*=
			/=




		*/

		// Use of  = Operator

			int value1  = 10;

		// use of += Operator

			value1 += 1; // output 11

		// use of -= Operator

			int  value2 = 10;

			value2 -= 1; // output 9

		// use of *= Operator

			value1 *= 2; //output 22

		// use of /= Operator

			value2  /= 2 ; //output 4

	}
}