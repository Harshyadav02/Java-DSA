// Variables in java 

 class _01_variables{
	public static void main(String args[]){
		// byte range is -128 to 127 an 1 byte is equal to 8 bit
		byte byte_variable = 111;

		

		// Short range is -32768 to -32767
		short short_variable = 23;
		
		// int range is -2147483648 to 2147483647
		int interger_variable = 10;

		// float range is upto 7 decimal
		float float_variable = 22f;

		// double range is upto 16 decimal value
		double double_variable = 24;


		String string_variable = "Harsh Yadav";

		// Char range is 0 to 255
		char  char_variable = 'a';

		// Boolean contain two values true and false (by default false)
		boolean boolean_variable = true; 



		System.out.println(byte_variable);
		System.out.println(short_variable);
		System.out.println(interger_variable);
		System.out.println(float_variable);
		System.out.println(double_variable);
		System.out.println(string_variable);
		System.out.println(char_variable);
		System.out.println(boolean_variable);


	}
}