class _07_Type_Casting{
	public static void main(String args[]){
		float float_value = 99.99f;
		// converting float value to integer
		int integer_value = (int)float_value;


		// converting int value to float
		int int_value = 10;
		float floating_value = (float)int_value;
		System.out.println(integer_value);
		System.out.println(floating_value);
	}
}
